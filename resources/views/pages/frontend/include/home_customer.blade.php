				var str = $(this).val();
				if($.trim(str) != ""){
					$.ajax({
					url: "http://www.sukhadaam.com/demo/sawlii/ajaxFreelancerSearch",
					cache: false,
					async : true,
					type: "GET",
					dataType: "json",
					data: { action:"get_typehead", query: $.trim(str),type : 'customer'},
					success: function(result){

						var strdata = '';
						$.each(result,function(i,data){

						if(data.type == 'title'){
							strdata += '<div class="auto_complete_item auto_complete_title"  data-url="#">'+data.name+'</div>';
						}else if(data.type == 'subtitle'){
							strdata += '<div class="auto_complete_item auto_complete_subtitle"  data-url="http://www.sukhadaam.com/demo/sawlii/'+data.slug+'">'+data.name+'</div>';
						}else{
							strdata += '<div class="auto_complete_item auto_complete_record"  data-url="http://www.sukhadaam.com/demo/sawlii/'+data.slug+'">'+data.name+'</div>';
						}
						});
					//console.log(strdata);
					$('.auto_complete_results').html(strdata);
					$('.auto_complete_results').css('display','block');
					$(".auto_complete_results").mCustomScrollbar();
					}
				});
				}else{
					$('.auto_complete_results').css('display','none');
					$('.auto_complete_results').html('');
				}
				}); 
